package pacote01;

class Inimigo {

    public int vida;
    public int dano;
    public int defesa;


    public void setVida(int vida) {
        this.vida = vida;
    }

    public void setDano(int dano) {
        this.dano = dano;
    }

    public void setDefesa(int defesa) {
        this.defesa = defesa;
    }


    public int getVida() {
        return this.vida;
    }

    public int getDano() {
        return this.dano;
    }

    public int getDefesa() {
        return this.defesa;
    }

}